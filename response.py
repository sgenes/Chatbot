import requests, os

FB_API_URL = 'https://graph.facebook.com/v9.0/me/messages'


PAGE_ACCESS_TOKEN = os.environ.get('FACEBOOK_PAGE_TOKEN')

def send_message(recipient_id, text):
    """Send a response to Facebook"""
    payload = {
        'message': {
            'text': text
        },
        'recipient': {
            'id': recipient_id
        },
        'notification_type': 'regular'
    }

    auth = {
        'access_token': PAGE_ACCESS_TOKEN
    }

    response = requests.post(
        FB_API_URL,
        params=auth,
        json=payload
    )

    return response.json()